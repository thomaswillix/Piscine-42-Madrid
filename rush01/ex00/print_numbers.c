void	show_result(char **arr);

void	show_result(char **arr)
{
	int	i;

	i = 0;
	while (i < 5)
	{
		if (arr[0][i] == '4')
		{
			arr[1][i] = '4';
			arr[2][i] = '3';
			arr[3][i] = '2';
			arr[4][i] = '1';
		}	
		if (arr[i][0] == '4')
		{
			arr[i][1] = '4';
                        arr[i][2] = '3';
                        arr[i][3] = '2';
                        arr[i][4] = '1';
		}
		if (arr[5][i] == '4')
                {
                        arr[1][i] = '4';
                        arr[2][i] = '3';
                        arr[3][i] = '2';
                        arr[4][i] = '1';
                }

		if (arr[0][i] == '1')
			arr[1][i] = '4';
		if (arr[i][0] == '1')
			arr[i][1] = '4';

	}
}
