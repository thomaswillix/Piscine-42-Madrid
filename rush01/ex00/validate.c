bool	validate_corners(char	**arr);

bool	validate_pairs(char	**arr);

bool	validate_corners(char	**arr)
{
	if ((arr[0][1] == '1' && arr[1][0] != '1')
	|| ((arr[0][1] == '2' || arr[0][1] == '3') && arr[1][0] == '4')
	|| (arr[0][1] == '4' && arr[1][0] == '1'))
		return (false);
	if ((arr[0][4] == '1' && arr[1][5] != '1')
	|| ((arr[0][4] == '2' || arr[0][4] == '3') && arr[1][5] == '4')
	|| (arr[0][4] == '4' && arr[1][5] == '1'))
		return (false);
	if ((arr[5][1] == '1' && arr[4][0] != '1')
	|| ((arr[5][1] == '2' || arr[5][1] == '3') && arr[4][0] == '4')
	|| (arr[5][1] == '4' && arr[4][0] == '1'))
		return (false);
	if ((arr[5][4] == '1' && arr[4][5] != '1')
	|| ((arr[5][4] == '2' || arr[5][4] == '3') && arr[4][5] == '4')
	|| (arr[5][4] == '4' && arr[4][5] == '1'))
		return (false);
	return (true);
}

bool	validate_pairs(char	**arr)
{
	int	i;

	i = 1;
	while (i < 5)
	{
		if ((arr[0][i] == '1' && arr[5][i] == '1')
		|| (arr[0][i] == '2' && arr[5][i] == '4')
		|| (arr[0][i] == '3' && (arr[5][i] == '4' || arr[5][i] == '3'))
		|| (arr[0][i] == '4' && arr[5][i] != '1'))
			return (false);
		if ((arr[i][0] == '1' && arr[i][5] == '1')
		|| (arr[i][0] == '2' && arr[i][5] == '4')
		|| (arr[i][0] == '3' && (arr[i][5] == '4' || arr[i][5] == '3'))
		|| (arr[i][0] == '4' && arr[i][5] != '1'))
			return (false);
		i++;
	}
	return (true);
}
